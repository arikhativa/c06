#include <unistd.h>

void	ft_putstr(char *s)
{
	while (*s)
	{
		write(1, s, 1);
		++s;
	}
}

int	main(int ac, char **av)
{
	if (ac >= 0)
	{
		ft_putstr(av[0]);
		write(1, "\n", 1);
	}
	return (0);
}
